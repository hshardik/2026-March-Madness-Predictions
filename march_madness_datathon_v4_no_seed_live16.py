# ── 3.2  Engineered features ─────────────────────────────────────────
conf_hist_wins = hist.groupby('CONF')['WINS'].mean().to_dict()
hist['conf_avg_wins'] = hist['CONF'].map(conf_hist_wins)

print('Conference strength (top 10):')
