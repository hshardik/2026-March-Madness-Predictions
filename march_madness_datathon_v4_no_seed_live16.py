print('FINAL MODEL — Feature Importance (no SEED)')
print('=' * 65)
for feat, imp in gb_imp.head(15).items():
    bar = '█' * int(imp * 200)
    print(f'  {feat:>22s}: {imp:.4f}  {bar}')

fig, ax = plt.subplots(figsize=(12, 8))
